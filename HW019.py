n = int(input())

def draw(r,l):
    for i in range(r,0,-1):
        print('#',end='')
    for i in range(l,0,-1):
        print(str(i),end='')
    print()

def view(n):
    r=l=n
    for i in range(n):
        draw(r,l)
        r += 1
        l -= 1

view(n)